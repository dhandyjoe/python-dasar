
# String
string1 = "Hello Python"

# Integer
int1 = 1

# Boolean 
bool1 = True # false

# Float 
float1 = 1.2

# List -> Mutable ( indeks bisa diubah )
list1 = ["sd", 1, 1.2]
print(type(list1))

# Tuple -> Immutable ( indeks tidak bisa diubah ) 
tuple1 = (1, 3.2, "Dhandy")
print(type(tuple1))

# Dictionary -> menyimpan beberapa tipe data beserta value nya
dictionary1 = {"nama" : "Dhandy", "id" : 2}
print(type(dictionary1))
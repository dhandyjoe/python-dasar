
# Casting digunakan untuk mengubah tipe data dari variable

"""
	-> int() = mengubah tipe data ke integer
	-> float() = mengubah tipe data ke float
	-> str() = mengubah tipe data ke string
"""

x = int(1)   
y = int(2.8) 
z = int("3") 

print(type(x))
print(type(y))
print(type(z))

a = float(1)     
b = float(2.8)   
c = float("3")  
d = float("4.2") 

print(a)
print(b)
print(c)
print(d)